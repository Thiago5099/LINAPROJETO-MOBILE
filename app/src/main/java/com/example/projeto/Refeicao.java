package com.example.projeto;

public class Refeicao {
    public String tipo;
    public String nome;
    public String tempo;
    public String kcal;

    public Refeicao(String tipo, String nome, String tempo, String kcal) {
        this.tipo = tipo;
        this.nome = nome;
        this.tempo = tempo;
        this.kcal = kcal;
    }
}