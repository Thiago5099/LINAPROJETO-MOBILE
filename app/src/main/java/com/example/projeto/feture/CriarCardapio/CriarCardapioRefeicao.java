package com.example.projeto.feture.CriarCardapio;

public class CriarCardapioRefeicao {
    public String tipo;
    public String nome;
    public String tempo;
    public String kcal;

    public CriarCardapioRefeicao(String tipo, String nome, String tempo, String kcal) {
        this.tipo = tipo;
        this.nome = nome;
        this.tempo = tempo;
        this.kcal = kcal;
    }
}